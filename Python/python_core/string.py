intro = "My name is Vikrant Prajapati and I am learning Python. I am enjoying it a lot and I am looking forward to learning more about it in the future."

# Slice 

# slicing from start to given point of string
print(intro[:5]);
# slicing from given point to end of string
print(intro[5:]);
# slicing from given point to given point of string
print(intro[5:15]);

# length of string
print(len(intro));